    private void montaTabela() {
    ArrayList<Custo> c = cc.recuperarTodos();
        if (c == null) {
            System.out.println("Ocorreu um erro ao consultar as tarefas");
        } else {
        tblCusto.setModel(new AbstractTableModel() {
                @Override
                public String getColumnName(int column) {
                    switch (column) {
                        case 0:
                            return "ID Custo";
                        case 1:
                            return "ID Prdocução";
                        case 2:
                            return "ID Tipo de Custo";
                        case 3:
                            return "Valor";
                        default:
                            return "";
                    }
                }

                @Override
                public int getColumnCount() {
                    return 4;
                }

                @Override
                public int getRowCount() {
                    return c.size();
                }

                @Override
                public Object getValueAt(int rowIndex, int columnIndex) {
                    Custo t = c.get(rowIndex);

                    if (t != null) {
                        switch (columnIndex) {
                            case 0:
                                return t.getId();
                            case 1:
                                return t.getProducao().getId();
                            case 2:
                                return t.getTipoCusto().getId();
                            case 3:
                                return t.getValor();
                        }

                    }

                    return "n/d";
                }
            });
        }
    }

