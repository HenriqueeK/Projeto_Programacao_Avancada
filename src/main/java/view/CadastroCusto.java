    private void bntSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntSalvarActionPerformed
        double valor = Double.parseDouble(cxValor.getText().replace(",", "."));

        Producao producao = new Producao();
        ComboItem itemProducao = (ComboItem) comboProducao.getSelectedItem();
        producao.setId(itemProducao.getCodigo());


        TipoCusto tipoCusto = new TipoCusto();
        ComboItem itemTipoCusto = (ComboItem) comboTipoCusto.getSelectedItem();
        tipoCusto.setId(itemTipoCusto.getCodigo());


        Custo c = new Custo();
        c.setValor(valor);
        c.setProducao(producao);
        c.setTipoCusto(tipoCusto);

        boolean retorno = false;

        if (codigo == 0) {
            retorno = cc.salvar(c);
        } else {
            c.setId(codigo);
            retorno = cc.editar(c);
        }

        if (retorno) {
            if (codigo == 0) {
                JOptionPane.showMessageDialog(null, "Salvo com sucesso");
            } else {
                codigo = 0;
                JOptionPane.showMessageDialog(null, "Editado com sucesso");
            }

            cxValor.setText("");
            comboProducao.setSelectedIndex(0);
            comboTipoCusto.setSelectedIndex(0);

        } else {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro, verifique os logs.");
        }

        montaTabela();
    }//GEN-LAST:event_bntSalvarActionPerformed

    private void bntExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntExcluirActionPerformed
        String idString = String.valueOf(tblCusto.getValueAt(tblCusto.getSelectedRow(), 0));
        int id = Integer.parseInt(idString);

        boolean retorno = cc.excluir(id);
        if (retorno) {
            JOptionPane.showMessageDialog(null, "Registro excluído com sucesso!");
            montaTabela();
        } else {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro ao excluir!");
        }         // TODO add your handling code here:
    }//GEN-LAST:event_bntExcluirActionPerformed

    private void bntEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntEditarActionPerformed
        String idString = String.valueOf(tblCusto.getValueAt(tblCusto.getSelectedRow(), 0));
        int id = Integer.parseInt(idString);

        Custo c = cc.recuperarUm(id);
        if (c != null) {
            codigo = c.getId();

            CombosDAO dao = new CombosDAO();

            ComboItem itemProducao = new ComboItem();
            itemProducao.setCodigo(c.getProducao().getId());
            dao.definirItemCombo(comboProducao, itemProducao);

            ComboItem itemTipoCusto = new ComboItem();
            itemTipoCusto.setCodigo(c.getTipoCusto().getId());
            dao.definirItemCombo(comboTipoCusto, itemTipoCusto);

            cxValor.setText(String.valueOf(c.getValor()));

            custo.setSelectedIndex(0);
        } else {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro ao editar!");
        }         // TODO add your handling code here:
    }//GEN-LAST:event_bntEditarActionPerformed

